#!/usr/bin/env python3
"""Graft libpython into a bin-bindings wheel so its binaries run on
machines without the build python.

The build links libpython at an absolute path that only exists on the
build machine. This copies the shared library into the wheel's scripts
dir (installers drop scripts and the lib into the same bin/) and points
the binaries at it: @executable_path + re-sign on macOS, $ORIGIN rpath
on Linux (needs patchelf). RECORD is regenerated by `wheel pack`.

Usage: repair-wheel.py WHEEL LIBPYTHON
"""

import glob
import os
import shutil
import subprocess
import sys
import tempfile

BINARIES = ("rikki", "tk")


def main() -> None:
    wheel, libpython = sys.argv[1], sys.argv[2]
    out_dir = os.path.dirname(os.path.abspath(wheel))
    lib = os.path.basename(libpython)
    with tempfile.TemporaryDirectory() as td:
        run([sys.executable, "-m", "wheel", "unpack", "-d", td, wheel])
        root = glob.glob(os.path.join(td, "*"))[0]
        scripts = glob.glob(os.path.join(root, "*.data", "scripts"))[0]
        # data/lib installs to <prefix>/lib, out of bin/ so installers do
        # not list the library as an executable
        libdir = os.path.join(os.path.dirname(scripts), "data", "lib")
        os.makedirs(libdir, exist_ok=True)
        shutil.copy2(libpython, libdir)
        os.chmod(os.path.join(libdir, lib), 0o755)
        for exe in BINARIES:
            path = os.path.join(scripts, exe)
            if sys.platform == "darwin":
                head = run(["otool", "-L", path], out=True)
                old = next(
                    line.split()[0]
                    for line in head.splitlines()[1:]
                    if "libpython" in line
                )
                run(
                    [
                        "install_name_tool",
                        "-change",
                        old,
                        f"@executable_path/../lib/{lib}",
                        path,
                    ]
                )
                # editing load commands invalidates the ad-hoc signature
                run(["codesign", "-f", "-s", "-", path])
            else:
                run(["patchelf", "--set-rpath", "$ORIGIN/../lib", path])
        run([sys.executable, "-m", "wheel", "pack", "-d", out_dir, root])


def run(cmd: list[str], out: bool = False) -> str:
    r = subprocess.run(cmd, capture_output=out, text=True, check=True)
    return r.stdout if out else ""


if __name__ == "__main__":
    main()
